# ── Helpers ─────────────────────────────────────────────────────────────────

def _decode_jwt_claims(token: str) -> dict:
    """Decode JWT payload (no signature verification — for logging only)."""
    try:
        payload = token.split('.')[1]
        padding = 4 - len(payload) % 4
        payload += '=' * padding
        return json.loads(base64.urlsafe_b64decode(payload))
    except Exception:
        return {}


def _get_credential() -> ClientSecretCredential:
    tenant = get_config('TENANT_ID') or ''
    client = get_config('CLIENT_ID') or ''
    secret = get_config('CLIENT_SECRET') or ''
    if not all([tenant, client, secret]):
        raise RuntimeError('Missing TENANT_ID, CLIENT_ID, or CLIENT_SECRET in config')
    return ClientSecretCredential(
        tenant_id=tenant,
        client_id=client,
        client_secret=secret,
    )


def _foundry_token_provider() -> str:
    """Token provider callback for AzureOpenAI (direct completion fallback)."""
    cred = _get_credential()
    token = cred.get_token('https://cognitiveservices.azure.com/.default')
    return token.token


def _get_direct_client() -> AzureOpenAI:
    """Direct OpenAI client (fallback when agent is unavailable)."""
    endpoint = get_config('FOUNDRY_ENDPOINT')
    if not endpoint:
        raise RuntimeError('FOUNDRY_ENDPOINT not configured')
    return AzureOpenAI(
        azure_endpoint=endpoint,
        azure_ad_token_provider=_foundry_token_provider,
        api_version='2024-12-01-preview',
    )


# ── Agent path (MCP tools) ─────────────────────────────────────────────────

def ask_agent(
    question: str,
    history: list[dict] | None = None,
    *,
    user_token: str | None = None,
    triage_token: str | None = None,
) -> dict:
    """Send a question to the Foundry Responses API with Sentinel MCP tools.

    Falls back to direct completion if MCP is unavailable.
    Returns {"answer": str, "kql": str|None, "results": list|None, "error": str|None}
    """
    project_endpoint = get_config('FOUNDRY_PROJECT_ENDPOINT')
    deployment = get_config('FOUNDRY_DEPLOYMENT') or ''
    if not project_endpoint:
        return ask_assistant(question, history)

    try:
        credential = _get_credential()

        # SP token for Data Lake server
        sp_token = credential.get_token(SENTINEL_MCP_SCOPE).token
        sp_claims = _decode_jwt_claims(sp_token)
        log.info('Data Lake SP token claims: %s', sp_claims)
        if triage_token:
            triage_claims = _decode_jwt_claims(triage_token)
            log.info('Triage delegated token claims: %s', triage_claims)

        # Build MCP tools — SP token for Data Lake, triage token for Triage
        tools = []
        for server in SENTINEL_MCP_SERVERS:
            auth_type = server.get('auth_type', 'sp')
            if auth_type == 'user':
                if not triage_token:
                    log.info('Triage MCP skipped — no delegated triage token available')
                    continue
                token = triage_token
                log.info('Triage MCP included with delegated triage token')
            else:
                token = sp_token
            tool_config = {k: v for k, v in server.items() if k != 'auth_type'}
            tool_config['authorization'] = token
            tools.append(tool_config)

        client = AIProjectClient(
            endpoint=project_endpoint,
            credential=credential,
        )
        oai = client.get_openai_client()

        # Build conversation input
        messages = []
        for msg in (history or []):
            messages.append({'role': msg['role'], 'content': msg['content']})
        messages.append({'role': 'user', 'content': question})

        def _invoke(tool_list: list) -> dict:
            resp = oai.responses.create(
                model=deployment,
                instructions=AGENT_INSTRUCTIONS,
                input=messages,
                tools=tool_list,
                timeout=90,
            )
            item_types = [getattr(i, 'type', 'unknown') for i in resp.output]
            log.info('Agent response items: %s', item_types)
            calls = [getattr(i, 'name', '') for i in resp.output if getattr(i, 'type', '') == 'tool_call']
            if calls:
                log.info('MCP tools called: %s', calls)
            answer = ''
            for item in resp.output:
                if getattr(item, 'type', '') == 'message':
                    for part in (getattr(item, 'content', None) or []):
                        text = getattr(part, 'text', None)
                        if text:
                            answer += text
            log.info('Agent answer length: %d chars', len(answer))
            kql = None
            if '```kql' in answer:
                try:
                    start = answer.index('```kql') + 6
                    end = answer.index('```', start)
                    kql = answer[start:end].strip()
                except ValueError:
                    pass
            return {'answer': answer, 'kql': kql, 'results': None, 'error': None}

        def _invoke_with_fallback(tool_list: list) -> dict:
            try:
                return _invoke(tool_list)
            except Exception as exc:
                exc_str = str(exc)
                is_401 = '401' in exc_str or 'Unauthorized' in exc_str
                if not is_401:
                    raise

                log.warning('MCP 401 detail: %s', exc_str[:500])
                triage_401 = 'sentinel-triage' in exc_str
                datalake_401 = 'sentinel-data-exploration' in exc_str
                has_triage = any(t.get('server_label') == 'sentinel-triage' for t in tool_list)
                has_datalake = any(t.get('server_label') == 'sentinel-data-exploration' for t in tool_list)

                if triage_401 and has_triage and has_datalake:
                    log.warning('Triage MCP 401 — retrying with Data Lake tools only')
                    return _invoke([t for t in tool_list if t.get('server_label') != 'sentinel-triage'])
                if datalake_401 and has_datalake and has_triage:
                    log.warning('Data Lake MCP 401 — retrying with Triage tools only')
                    return _invoke([t for t in tool_list if t.get('server_label') != 'sentinel-data-exploration'])

                log.warning('All MCP servers unavailable — retrying agent with no tools')
                return _invoke([])

        result = _invoke_with_fallback(tools)
        client.close()
        return result

    except Exception as exc:
        log.warning('Agent invocation failed (%s), falling back to direct completion', exc)
        return ask_assistant(question, history)


# ── KQL repair ──────────────────────────────────────────────────────────────

_KQL_COLUMN_FIXES = {
    'CreatedTimeUtc': 'TimeGenerated',
    'LastModifiedTimeUtc': 'LastModifiedTime',
}


def _repair_kql(kql: str) -> str:
    """Apply known column-name fixes to LLM-generated KQL before execution."""
    for bad, good in _KQL_COLUMN_FIXES.items():
        if bad in kql:
            log.warning("KQL repair: replacing '%s' with '%s'", bad, good)
            kql = kql.replace(bad, good)
    return kql


# ── Direct completion fallback ──────────────────────────────────────────────

def ask_assistant(question: str, history: list[dict] | None = None) -> dict:
    """Fallback: direct OpenAI completion with KQL auto-execute.

    Returns {"answer": str, "kql": str|None, "results": list|None, "error": str|None}
    """
    deployment = get_config('FOUNDRY_DEPLOYMENT')
    if not deployment:
        return {'answer': '', 'kql': None, 'results': None,
                'error': 'AI Assistant not configured — set FOUNDRY_ENDPOINT and FOUNDRY_DEPLOYMENT in Settings.'}

    messages: list = [{'role': 'system', 'content': SYSTEM_PROMPT}]
    for msg in (history or []):
        messages.append({'role': msg['role'], 'content': msg['content']})
    messages.append({'role': 'user', 'content': question})

    client = _get_direct_client()
    completion = client.chat.completions.create(
        model=deployment,
        messages=messages,
        max_completion_tokens=2048,
    )

    answer = completion.choices[0].message.content or ''

    kql = None
    results = None
    error = None
    if '```kql' in answer:
        try:
            start = answer.index('```kql') + 6
            end = answer.index('```', start)
            kql = answer[start:end].strip()
        except ValueError:
            pass

        if kql:
            kql = _repair_kql(kql)
            log.info('Auto-executing generated KQL:\n%s', kql)
            try:
                results = run_kql(kql)
            except Exception as exc:
                log.warning('Generated KQL failed: %s', exc)
                error = str(exc)

    return {'answer': answer, 'kql': kql, 'results': results, 'error': error}
